package com.nt.vo;

public class AccHolderVO {

	private String accNo;
	private String accHolderName;
	private String accHolderAdd;
	private String principalAmt;
	private String time;
	private String rateOfInterest;
	
	public String getAccNo() {
		return accNo;
	}
	public void setAccNo(String accNo) {
		this.accNo = accNo;
	}
	public String getAccHolderName() {
		return accHolderName;
	}
	public void setAccHolderName(String accHolderName) {
		this.accHolderName = accHolderName;
	}
	public String getAccHolderAdd() {
		return accHolderAdd;
	}
	public void setAccHolderAdd(String accHolderAdd) {
		this.accHolderAdd = accHolderAdd;
	}
	public String getPrincipalAmt() {
		return principalAmt;
	}
	public void setPrincipalAmt(String principalAmt) {
		this.principalAmt = principalAmt;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	public String getRateOfInterest() {
		return rateOfInterest;
	}
	public void setRateOfInterest(String rateOfInterest) {
		this.rateOfInterest = rateOfInterest;
	}
}
